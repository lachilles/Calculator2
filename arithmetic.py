def add(num1, num2):
    """ Adds num1 to num2 """
    return int(num1) + int(num2)
    
def subtract(num1, num2):
    """ Subtracts num1 to num2 """    
    return int(num1) - int(num2)

def multiply(num1, num2):
    """ Multiplies num1 to num2 """

    return int(num1) * int(num2)

def divide(num1, num2):
    """ Divides num1 to num2 """
    
    return int(num1) / int(num2)

def square(num1):
    """ Squares num1 """

    return int(num1) * int(num1)

def cube(num1):
    """ Cubes num1 """

    return int(num1) * int(num1) * int(num1) 

def power(num1, num2):
    """ Raise num1 to the power of num2 """

    return pow(int(num1), int(num2))

def mod(num1, num2):
    """ Return the remainder when num1 is divided by num2 """

    return int(num1) % int(num2)
